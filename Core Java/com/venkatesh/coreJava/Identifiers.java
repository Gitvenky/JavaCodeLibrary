package com.venkatesh.coreJava;

public class Identifiers {
	/* Identifiers 		 ==> Class name, method name, variable name and label name.
	 * allowed chars are ==> a-z, A-Z, 0-9, $, _.
	 * Identifiers can't start with digits.
	 * Identifiers are case sensitive.
	 * No length Limit for Identifiers.
	 * we can't use reserved keywords as identifiers.
	 * we can use predefined java class names as keywords
	 * 
	 * */
	
	
		
	//valid identifiers are the below
	int kkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkkk=999;
	int total_number=999, java2share=999, Runnable = 999,
			ca$h=999, Int=999, Integer=999, _$_$=999, total123=999;
	//invalid identifiers are below
	/*
	 * int 123total=999; int all@hands=333; int int=888;
	 */
	
	
}
